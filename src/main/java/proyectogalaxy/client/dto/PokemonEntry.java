package proyectogalaxy.client.dto;

public class PokemonEntry {

    private String name;
    private String url;

    public PokemonEntry() {
    }

    public String getName() {

        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }


}
